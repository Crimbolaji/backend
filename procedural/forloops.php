<?php 
// For loop in PHP
// A for loop is a more efficient loop structure in programming language.
// In for loop, we have passed three parameters, initialize, truth expression and increment expression.

// Syntax of the for loop
// for(initialization; truth expression; increment expression){
//     statements;
// }

// The truth expression is a boolean expression that tests and 
// compares the counter to a fixed value after each iteration and 
// halting the for loop when false is returned. 
// The incrementation/decrementation increases (or decreases) the counter by a set value.

for ($x =0; $x <10; $x++){
    echo "The number is " . $x . "<br/>";
}
    

?>